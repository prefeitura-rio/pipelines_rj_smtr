@task
def save_redis(key, files):
    redis_client = get_redis_client()
    content = redis_client.get(key)
    if not content:
        content = {}
    content["files"] = files
    log(f"saving {len(files)} in {key}: {files[-1]}")
    redis_client.set(key, content)


